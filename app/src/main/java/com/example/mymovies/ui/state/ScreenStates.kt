package com.example.mymovies.ui.state

import androidx.compose.foundation.BorderStroke
import androidx.compose.foundation.layout.Arrangement
import androidx.compose.foundation.layout.Column
import androidx.compose.foundation.layout.fillMaxWidth
import androidx.compose.foundation.layout.padding
import androidx.compose.foundation.layout.wrapContentWidth
import androidx.compose.material3.ButtonDefaults
import androidx.compose.material3.CircularProgressIndicator
import androidx.compose.material3.MaterialTheme
import androidx.compose.material3.OutlinedButton
import androidx.compose.material3.Text
import androidx.compose.runtime.Composable
import androidx.compose.ui.Alignment
import androidx.compose.ui.Modifier
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.res.stringResource
import androidx.compose.ui.text.font.FontWeight
import androidx.compose.ui.unit.dp
import com.example.mymovies.R
import com.example.mymovies.data.model.ErrorState

@Composable
fun LoadingView(
    modifier: Modifier = Modifier
) {
    Column(
        modifier = modifier,
        verticalArrangement = Arrangement.Center,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        CircularProgressIndicator()
    }
}

@Composable
fun LoadingItem() {
    CircularProgressIndicator(
        modifier = Modifier
            .fillMaxWidth()
            .padding(16.dp)
            .wrapContentWidth(Alignment.CenterHorizontally)
    )
}

@Composable
fun ErrorItem(
    errorState: ErrorState,
    modifier: Modifier = Modifier,
    onClickRetry: () -> Unit
) {
    Column(
        modifier = modifier.padding(16.dp),
        horizontalAlignment = Alignment.CenterHorizontally,
        verticalArrangement = Arrangement.Center
    ) {
        Text(
            text = errorState.toErrorMessage(),
            maxLines = 1,
            style = MaterialTheme.typography.titleMedium,
            color = Color.Red,
            modifier = Modifier.padding(bottom = 24.dp)
        )
        OutlinedButton(
            onClick = onClickRetry, border = BorderStroke(2.dp, Color.DarkGray),
            colors = ButtonDefaults.outlinedButtonColors(
                contentColor = Color.White,
                containerColor = Color.DarkGray
            ),
        ) {
            Text(text = stringResource(id = R.string.try_again), fontWeight = FontWeight.Bold)
        }
    }
}

@Composable
fun ErrorState.toErrorMessage(): String {
    return when (this) {
        ErrorState.InternetConnectionError -> {
            stringResource(id = R.string.no_network)
        }

        else -> {
            stringResource(id = R.string.unknown_error)
        }
    }
}